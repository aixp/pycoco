   @staticmethod
   def LeadingAny( p ):
      assert isinstance( p, Node) or (p is None)
      if p is None:
         return None
      a = None
      if p.typ == Node.any:
         a = p
      elif p.typ == Node.alt:
         a = Tab.LeadingAny(p.sub)
         if a is None:
            a = Tab.LeadingAny(p.down)
      elif p.typ == Node.opt or p.typ == Node.iter:
         a = Tab.LeadingAny(p.sub)
      elif Node.DelNode(p) and not p.up:
         a = Tab.LeadingAny(p.next)
      return a

   @staticmethod
   def FindAS( p ):
      '''find ANY sets'''
      assert isinstance( p, Node ) or ( p is None )
      while p is not None:
         if p.typ == Node.opt or p.typ == Node.iter:
            Tab.FindAS(p.sub)
            a = Tab.LeadingAny(p.sub)
            if a is not None:
               a.set -= Tab.First(p.next)
         elif p.typ == Node.alt:
            s1 = set( )
            q = p
            while q is not None:
               Tab.FindAS(q.sub)
               a = Tab.LeadingAny(q.sub)
               if a is not None:
                  h = Tab.First(q.down)
                  h |= s1
                  a.set -= h
               else:
                  s1 |= Tab.First(q.sub)
               q = q.down
         if p.up:
            break
         p = p.next;

