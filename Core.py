      if sym.n not in Tab.visited:
         Tab.visited.add(sym.n)
         nt = Symbol.nonterminals
         for s in nt:
            if s.n in sym.nts:
               Tab.Complete(s)
               sym.follow |= s.follow
               if sym == Tab.curSy:
                  sym.nts.discard(s.n)

